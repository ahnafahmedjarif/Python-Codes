"""                                    variable in python                                  

In Python, a variable is a name that is used to refer to a value stored in memory. Variables are used to store data so that it can be referenced and manipulated throughout a program.   

"""

# Assigning values to variables
x = 5
y = "Hello world"
z = 3.14

# Printing the values of variables
print(x)  # Output: 5
print(y)  # Output: Hello world
print(z)  # Output: 3.14

# Variables can be reassigned with values of different types
x = "Python"
print(x)  # Output: Python
