"""                      Function                   

A function in programming is a self-contained block of code that performs a specific task or a related group of tasks. 

In Python, you can define functions using the `def` keyword followed by the function name, parameters (if any), and a colon.

 """

# Define a function that greets the user
def greet(name):
    print("Hello, ", name, "!")

# Call the function
greet("Jarif") # Output: Hello, Jarif!

# Define a function that adds two number
def add(n1, n2):
    return n1 + n2 #  `return` statement is used within a function to exit the function and optionally return a value back to the caller.

print(add(6, 60))

